#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int m;
	int n;

	m = ft_strlen(s1);
	n = 0;
	while (s2[n] != '\0')
	{
		s1[m + n] = s2[n];
		n++;
	}
	s1[m + n] = '\0';
	return (s1);
}
