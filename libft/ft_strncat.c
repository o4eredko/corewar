#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int m;
	int i;
	int o;

	o = (int)n;
	m = ft_strlen(s1);
	i = 0;
	while (s2[i] != '\0' && i < o)
	{
		s1[m + i] = s2[i];
		i++;
	}
	s1[m + i] = '\0';
	return (s1);
}
